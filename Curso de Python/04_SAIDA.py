# soma +
# subtração -
# multiplicação *
# divisão /

m = 20
s = 2

soma = m + s
sub  = m - s
mul  = m * s
div  = m / s # s diferete de 0
print(soma,sub,mul,div)
print("",soma,sub,mul,div)
print("soma = %.3f\nsubtração = %.2f\nmultiplicação = %.5f\ndivisão = %.6f"%(soma,sub,mul,div))
#print("%i %d %f %s")
print("\nFormaut")
print("soma = {}\nsubtração = {}\nmultiplicação = {}\ndivisão = {}\n\n".format(soma,sub,mul,div))
print("soma = {:.3f}\nsubtração = {:.2f}\nmultiplicação = {:.5f}\ndivisão = {:.6f}".format(soma,sub,mul,div))

